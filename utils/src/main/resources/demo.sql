DROP TABLE TREE;

CREATE TABLE TREE
(
    ID        NUMBER,
    NAME      VARCHAR2(500),
    DSC       VARCHAR2(2000),
    PARENT_ID NUMBER,
    ORDER_ID  NUMBER,
    LEVEL_ID  NUMBER
);

COMMENT ON table TREE is '树形结构表';
COMMENT ON COLUMN TREE.ID is '值';
COMMENT ON COLUMN TREE.NAME is '显示名称';
COMMENT ON COLUMN TREE.DSC is '描述';
COMMENT ON COLUMN TREE.PARENT_ID is '父节点ID';
COMMENT ON COLUMN TREE.ORDER_ID is '排序';
COMMENT ON COLUMN TREE.LEVEL_ID is '等级';

